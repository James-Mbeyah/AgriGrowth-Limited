            <h3>LINKING  FARMERS AND FOOD DISTRIBUTORS </h3>
            <p>There is high demand for farm produce in the country, but the farm produce are not available, and those that are available the prices are extremely high. Farmers have crops lying in the fields due to lack of market for their produce, this is because of lack of a proper channel that provide market to farmers.</p>
