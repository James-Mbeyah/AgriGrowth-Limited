            <p>AgriGrowth intends to connect farmers with food distributors who will buy their produce in bulk and at a good price for both the producer and the buyer.</p>
            <br>
            <h3>PEST AND DISEASES CONTROL INFO</h3>
            <p>Pests and diseases are a very big problem to the farmer, AgriGrowth intends to set up a platform where a farmer is able to access information of various crop Pests and diseases and identify what is affecting their crops and the solution.</p>
            
        </div>
        <div class="box">
            
            <h3>SAFE STORAGE OF HARVESTED FARM PRODUCE</h3>
            <p>AgriGrowth intends to set up collection centers across the country to ease the collection and transportation of farm produce from the farmer to the food distributor.
            Produce will be weighed in the presence of the farmer and the quantity updated into the system, the food distributor will be notified and will process the payment to the farmer.</p>
        </div>
        <div class="box">
            
            <h3>TRANSPORTATION SERVICES</h3>
            <p>Movement of produce from the farmer to the food distributor is key to both the farmer and the food distributor. AgriGrowth also provide transportation services to food distributors to their warehouses in various locations from our collection centers across the country.</p>
            <br>
            <h3>CLIMATE INFORMATION</h3>
            <p>AgriGrowth intends provides farmers with climate information thus helping them prepare for various climate change conditions on their farms.</p>
